public class Ifelse {
    public static void main(String[] args) {
        int salary = 10000;
        if(salary > 10000) {
            System.out.println(salary + 2000);
        }else if(salary > 20000) {
            System.out.println(salary + 3000);
        }else {
            System.out.println(salary + 1000);
        }

    }
}
